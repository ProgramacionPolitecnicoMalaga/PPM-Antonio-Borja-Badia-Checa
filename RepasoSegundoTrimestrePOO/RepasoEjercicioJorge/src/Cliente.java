public class Cliente {

    String nombre, apellido, DNI, fechNacimiento, numTelefono, direccion;

    public Cliente(String nombre, String apellido, String DNI, String fechNacimiento, String numTelefono, String direccion) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.DNI = DNI;
        this.fechNacimiento = fechNacimiento;
        this.numTelefono = numTelefono;
        this.direccion = direccion;
    }




}

public class Plato {

    String plato_tipico, descripcion, ciudad_origen;

    public Plato(String plato_tipico, String descripcion, String ciudad_origen) {
        this.plato_tipico = plato_tipico;
        this.descripcion = descripcion;
        this.ciudad_origen = ciudad_origen;
    }

    public String getPlato_tipico() {
        return plato_tipico;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public String getCiudad_origen() {
        return ciudad_origen;
    }




}
